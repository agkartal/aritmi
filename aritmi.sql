CREATE TABLE USERS
(
	ID INT AUTO_INCREMENT PRIMARY KEY,
	USERNAME VARCHAR(50),
	PASSWORD VARCHAR(50),
	NAME VARCHAR(50)
)
DEFAULT CHARSET=utf8 DEFAULT COLLATE utf8_unicode_ci;

